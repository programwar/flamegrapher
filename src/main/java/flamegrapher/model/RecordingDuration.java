package flamegrapher.model;

public class RecordingDuration {

    private Long startMilliseconds;
    private Long endMilliseconcs;

    public Long getStartMilliseconds() {
        return startMilliseconds;
    }

    public void setStartMilliseconds(Long startMilliseconds) {
        this.startMilliseconds = startMilliseconds;
    }

    public Long getEndMilliseconcs() {
        return endMilliseconcs;
    }

    public void setEndMilliseconcs(Long endMilliseconcs) {
        this.endMilliseconcs = endMilliseconcs;
    }

}
